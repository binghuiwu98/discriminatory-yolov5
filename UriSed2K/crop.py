for index in range(len(dataset)):
    img_id = dataset.ids[index]
    img = Image.open(datadir + '/images/%s.jpg' % img_id).convert("RGB")
    annos = dataset.get_groundtruth(index)
    crops, crop_labels, crop_xywh = get_closeup(img, annos)
    for crop, label, xywh in list(zip(crops, crop_labels, crop_xywh)):
        # label = PascalVOCDataset.CLASSES[label]
        label = dataset.categories[label]
        cls_count[label] += 1
        crop.save(datadir + '/Crops/%s/%s_%d.jpg' % (label, label, cls_count[label]))
        f = open(os.path.join(datadir, 'Crops/labels_%s/%s_%d.txt' % (label, label, cls_count[label])), 'w')
        f.write('%d %f %f %f %f\n' % (PascalVOCDataset.CLASSES.index(label) - 1, xywh[0], xywh[1], xywh[2], xywh[3]))
        f.close()
